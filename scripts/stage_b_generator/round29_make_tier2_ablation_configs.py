"""Generate Round-29 Tier-2 ablation configs from the a0 baseline.

Tier-2 ablations test three design-philosophy mismatches identified in
the audit at `analyses/2026-05-26_round29_cond_and_loss_audit.md`:

  1. init_pose (frame-0 SMPL-22 joints) — duplicates info already in
     motion[0] + stage1_coarse[0]; plus a train/inference distribution
     leak (training sees clean GT frame 0, inference's Stage-1 frame 0
     is noisy).
  2. text (CLIP per-token + text_xattn) — InterAct text labels are
     object names that object_tokens already encodes; three cross-
     attentions per block is more than needed.
  3. pos_loss_weight (dense FK position MSE, weight 5) — overlaps
     mse_x0 (motion_135 channels) and loss_anchor_joint_pos (active
     parts, weight 10).

Each ablation is a single-variable change from r29_a0_input_add (FULL-DENSE
+ J0 input_add). Same training subset, same epochs, same R29 4-family
content. Compare A0 baseline diag results vs each ablation's diag results
on the same 48-clip subset.

Outputs:
    configs/training/anchordiff_r29_t2_no_init_pose.yaml
    configs/training/anchordiff_r29_t2_no_text.yaml
    configs/training/anchordiff_r29_t2_no_dense_pos.yaml
    analyses/round29_tier2_ablation_manifest.json
    analyses/round29_tier2_ablation_manifest.md

Usage:
    python scripts/stage_b_generator/round29_make_tier2_ablation_configs.py
    python scripts/stage_b_generator/round29_make_tier2_ablation_configs.py \
        --data-root /media/.../piano_official_process_4
"""
