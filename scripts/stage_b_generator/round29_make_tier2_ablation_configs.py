def _to_posix_relpath(p: str | Path, root: Path) -> str:
    s = str(p)
    if not s:
        return s
    p_obj = Path(s)
    try:
        rel = p_obj.resolve().relative_to(root.resolve())
        return rel.as_posix()
    except (ValueError, OSError):
        return p_obj.as_posix() if not p_obj.is_absolute() else s


def main() -> int:
    parser = argparse.ArgumentParser(
        description="Generate R29 Tier-2 ablation configs (init_pose / text / dense_pos).",
    )
    parser.add_argument("--dry-run", action="store_true")
    parser.add_argument(
        "--data-root",
        default=os.environ.get("DATASETS_ROOT", DEFAULT_DATA_ROOT),
        help=(
            "Root directory containing the four InterAct subsets. "
            "On the Linux server pass --data-root /media/.../piano_official_process_4 "
            "or export DATASETS_ROOT=..."
        ),
    )
    parser.add_argument(
        "--subset-file", default=DEFAULT_SUBSET_FILE,
        help="Subset JSON consumed by trainer + diagnostics.",
    )
    parser.add_argument(
        "--config-dir", default=str(DEFAULT_CONFIG_DIR),
    )
    parser.add_argument(
        "--analyses-dir", default=str(DEFAULT_ANALYSES_DIR),
    )
    args = parser.parse_args()

    config_dir = Path(args.config_dir)
    analyses_dir = Path(args.analyses_dir)
    subset_file = _to_posix_relpath(args.subset_file, ROOT)

    rows: list[dict] = []
    for v in VARIANTS:
        out_path = config_dir / f"anchordiff_{v.variant_id}.yaml"
        content = _render_yaml(v, subset_file=subset_file, data_root=args.data_root)
        canonical_cfg = f"configs/training/anchordiff_{v.variant_id}.yaml"
        row = {
            "variant_id": v.variant_id,
            "group": "Tier2",
            "purpose": v.purpose,
            # Baseline comparison: r29_a0_input_add must be already trained.
            "baseline_variant_id": "r29_a0_input_add",
            "knobs": {
                "init_pose_dim": v.init_pose_dim,
                "text_dim": v.text_dim,
                "pos_loss_weight": v.pos_loss_weight,
                "hand_endpoint_weight": v.hand_endpoint_weight,
                "foot_endpoint_weight": v.foot_endpoint_weight,
            },
            "subset_kind": "balanced",
            "subset_file": subset_file,
            "num_epochs": 300,
            "seed": 42,
            "val_on_train_subset": True,
            "config_path": canonical_cfg,
            "output_dir": f"runs/training/stageB_anchordiff_{v.variant_id}",
            "diagnostics": list(v.diagnostics),
        }
        rows.append(row)
        if args.dry_run:
            print(f"DRY-RUN would write: {out_path}  ({len(content)} bytes)")
        else:
            out_path.parent.mkdir(parents=True, exist_ok=True)
            out_path.write_text(content, encoding="utf-8")
            print(f"wrote {out_path}")

    manifest_json = analyses_dir / "round29_tier2_ablation_manifest.json"
    manifest_md = analyses_dir / "round29_tier2_ablation_manifest.md"

    md_lines: list[str] = [
        "# Round-29 Tier-2 ablation manifest",
        "",
        "Each variant is a single-variable change from `r29_a0_input_add` (FULL-DENSE + J0).",
        "",
        "| variant | knob | value | baseline value |",
        "| --- | --- | --- | --- |",
        "| `r29_t2_no_init_pose` | `init_pose_dim` | `0` | `66` |",
        "| `r29_t2_no_text` | `text_dim` | `0` | `512` |",
        "| `r29_t2_no_dense_pos` | `pos_loss_weight` | `0.0` | `5.0` |",
        "| | `hand_endpoint_weight` | `1.0` | `2.0` |",
        "| | `foot_endpoint_weight` | `1.0` | `2.0` |",
        "",
        "Compare each variant's three diag outputs against `r29_a0_input_add`.",
    ]
    if args.dry_run:
        print(f"DRY-RUN would write: {manifest_json}")
        print(f"DRY-RUN would write: {manifest_md}")
    else:
        analyses_dir.mkdir(parents=True, exist_ok=True)
        manifest_json.write_text(
            json.dumps({"variants": rows, "data_root": args.data_root}, indent=2),
            encoding="utf-8",
        )
        manifest_md.write_text("\n".join(md_lines) + "\n", encoding="utf-8")
        print(f"wrote {manifest_json}")
        print(f"wrote {manifest_md}")
    print(f"\n{len(rows)} Tier-2 variant(s) emitted.")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
