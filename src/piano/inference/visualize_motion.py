"""Motion visualization tool: render skeleton animations as MP4.

Supports two input sources:
    1. Real preprocessed dataset samples (from HOIDataset output)
    2. Generated motion from a smoke-test / inference run (.npz file)

Usage examples:

    # Visualize 4 real sequences from the preprocessed dataset
    python -m piano.inference.visualize_motion real \\
        --data-dir /media/.../datasets/omomo/piano \\
        --seq-ids sub10_clothesstand_000 sub11_largebox_003 sub12_monitor_001 \\
        --output-dir runs/visualizations/2026-04-19_real_samples

    # Visualize motion generated by the smoke test
    python -m piano.inference.visualize_motion generated \\
        --run-dir runs/checks/inference_smoke_test/2026-04-19_063940 \\
        --output-dir runs/visualizations/2026-04-19_smoke_generated
"""
from __future__ import annotations

import argparse
from datetime import datetime
from pathlib import Path

import numpy as np

from piano.utils.io_utils import ensure_dir, load_json, save_json


# ---------------------------------------------------------------------------
# SMPL 22-joint skeleton for rendering
# ---------------------------------------------------------------------------

SKELETON_CONNECTIONS: list[tuple[int, int]] = [
    (0, 1), (0, 2), (0, 3),        # pelvis → left_hip, right_hip, spine1
    (1, 4), (2, 5), (3, 6),        # hips → knees, spine1 → spine2
    (4, 7), (5, 8), (6, 9),        # knees → ankles, spine2 → spine3
    (7, 10), (8, 11),              # ankles → feet
    (9, 12), (9, 13), (9, 14),     # spine3 → neck, collars
    (12, 15),                      # neck → head
    (13, 16), (14, 17),            # collars → shoulders
    (16, 18), (17, 19),            # shoulders → elbows
    (18, 20), (19, 21),            # elbows → wrists
]


# ---------------------------------------------------------------------------
# Motion decoding
# ---------------------------------------------------------------------------

def motion_263_to_joints(
    motion: np.ndarray,
    fps: float = 20.0,
    mean: np.ndarray | None = None,
    std: np.ndarray | None = None,
    world_R_y_angle: float | None = None,
    world_T_xz: np.ndarray | None = None,
) -> np.ndarray:
    """Reconstruct 22 absolute joint positions from HumanML3D 263-dim features.

    Delegates to MoMask's official ``recover_from_ric`` helper so the
    heading rotation, per-joint XZ offsets and absolute Y handling are
    all correct. Naive "subtract root, add height" code does not work
    because HumanML3D canonicalizes the heading before encoding.

    Parameters
    ----------
    motion : (T, 263) — HumanML3D features
    fps : unused (kept for API compatibility; recovery is frame-rate agnostic)
    mean, std : (263,) optional — denormalization stats applied before recovery
    world_R_y_angle, world_T_xz : optional — per-clip canonical→world
        transform (Y-axis rotation in radians, XZ translation 2-vector).
        When supplied, joints recovered in HumanML3D canonical frame are
        rotated by R_y around +Y and translated by (T_x, 0, T_z) so they
        sit in the SOURCE clip's world frame. This is what lets a
        canonical-frame generated motion be visualised together with a
        world-frame object trajectory (without it the body always starts
        at canonical origin while the object is metres away). Computed
        in :func:`scripts/stage_b_generator/qual_eval._get_canon_to_world_transform`.

    Returns
    -------
    joints : (T, 22, 3) — absolute joint positions, in world frame when
        ``world_R_y_angle`` + ``world_T_xz`` were provided, else in
        canonical frame.
    """
    import torch

    # MoMask's utils imports expect sys.path to include the backbone repo,
    # which the adapter already sets up when first imported.
    import piano.models.backbones.momask_adapter  # noqa: F401 — path side-effect
    from utils.motion_process import recover_from_ric

    if mean is not None and std is not None:
        motion = motion * std + mean

    data = torch.from_numpy(motion).float().unsqueeze(0)  # (1, T, 263)
    joints = recover_from_ric(data, joints_num=22)         # (1, T, 22, 3)
    joints = joints.squeeze(0).cpu().numpy().astype(np.float32)

    if world_R_y_angle is not None and world_T_xz is not None:
        cos_a = float(np.cos(world_R_y_angle))
        sin_a = float(np.sin(world_R_y_angle))
        R = np.array(
            [[cos_a, 0.0, sin_a],
             [0.0,   1.0, 0.0],
             [-sin_a, 0.0, cos_a]],
            dtype=np.float32,
        )
        joints = joints @ R.T            # (T, 22, 3)
        joints[..., 0] += float(world_T_xz[0])
        joints[..., 2] += float(world_T_xz[1])

    return joints


def _axis_angle_to_rotmat(aa: np.ndarray) -> np.ndarray:
    """Rodrigues: (3,) axis-angle to (3, 3) rotation matrix."""
    theta = np.linalg.norm(aa)
    if theta < 1e-8:
        return np.eye(3, dtype=np.float32)
    axis = aa / theta
    K = np.array(
        [
            [0, -axis[2], axis[1]],
            [axis[2], 0, -axis[0]],
            [-axis[1], axis[0], 0],
        ],
        dtype=np.float32,
    )
    return (
        np.eye(3, dtype=np.float32)
        + np.sin(theta) * K
        + (1 - np.cos(theta)) * K @ K
    )


