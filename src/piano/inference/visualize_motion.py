def run_visualization(
    samples: list[dict],
    output_dir: Path,
    source: str,
    fps: float,
    mean: np.ndarray | None = None,
    std: np.ndarray | None = None,
    use_recovery: bool = False,
) -> None:
    """Render motion videos.

    Parameters
    ----------
    use_recovery : if True, always decode via ``motion_263_to_joints``
        (MoMask's ``recover_from_ric``). For real samples this provides
        an encode → decode round-trip sanity check: if the new encoder
        produced HumanML3D-compatible features, the recovered skeleton
        should look normal (in canonical coordinate frame, not world).
        If False (default), real uses precomputed joints_22 directly.
    """
    output_dir = ensure_dir(output_dir)
    index: list[dict] = []

    for sample in samples:
        seq_id = sample["seq_id"]
        # Default: real uses precomputed joints_22 (world frame); generated
        # uses recover_from_ric (canonical frame). Override with use_recovery
        # to force recovery path on real samples for round-trip validation.
        if sample["joints_22"] is not None and not use_recovery:
            joints = sample["joints_22"]
        else:
            joints = motion_263_to_joints(
                sample["motion_263"], fps=fps, mean=mean, std=std,
                world_R_y_angle=sample.get("world_R_y_angle"),
                world_T_xz=sample.get("world_T_xz"),
            )

        # Clip joints to actual number of frames
        n_frames = sample["num_frames"]
        joints = joints[:n_frames]

        obj_pos = sample.get("object_positions")
        obj_rot = sample.get("object_rotations")
        obj_pc = sample.get("object_pc")
        if obj_pos is not None:
            obj_pos = obj_pos[:n_frames]
        if obj_rot is not None:
            obj_rot = obj_rot[:n_frames]

        title = f"[{source}] {seq_id}\n{sample['text'][:70]}"
        out_path = output_dir / f"{seq_id}.mp4"
        print(f"\nRendering {seq_id} ({n_frames} frames) → {out_path}")
        render_motion_video(
            joints, out_path, fps=fps, title=title,
            object_positions=obj_pos,
            object_rotations=obj_rot,
            object_pc=obj_pc,
        )
        index.append({
            "seq_id": seq_id,
            "text": sample["text"],
            "object_id": sample.get("object_id", ""),
            "num_frames": n_frames,
            "file": out_path.name,
            "source": source,
        })

    summary = {
        "timestamp": datetime.now().isoformat(),
        "source": source,
        "num_videos": len(index),
        "fps": fps,
        "videos": index,
    }
    save_json(output_dir / "summary.json", summary)
    print(f"\nRendered {len(index)} videos. Summary: {output_dir / 'summary.json'}")


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description=__doc__.split("\n\n")[0])
    subs = parser.add_subparsers(dest="source", required=True)

    # real
    p_real = subs.add_parser("real", help="Visualize real preprocessed sequences")
    p_real.add_argument("--data-dir", type=Path, required=True)
    p_real.add_argument("--seq-ids", nargs="*", default=None,
                        help="Specific sequence ids (default: first --num-samples)")
    p_real.add_argument("--num-samples", type=int, default=4)

    # generated
    p_gen = subs.add_parser("generated", help="Visualize generated motions from a run dir")
    p_gen.add_argument("--run-dir", type=Path, required=True,
                       help="A smoke-test or inference run directory containing generated.npz")
    p_gen.add_argument("--seq-ids", nargs="*", default=None,
                       help="Render only these sequence ids (default: render all clips). "
                            "Use this to spot-check a focused subset (worst clips, "
                            "historical hard cases, etc.) instead of rendering the full "
                            "80-clip eval matrix.")
    p_gen.add_argument("--num-samples", type=int, default=None,
                       help="If --seq-ids is not set, render only the first N clips. "
                            "Default: render all.")

    for p in (p_real, p_gen):
        p.add_argument("--output-dir", type=Path, default=None,
                       help="Default: runs/visualizations/<timestamp>/")
        p.add_argument("--fps", type=float, default=20.0)
        p.add_argument("--mean", type=Path, default=None,
                       help="HumanML3D mean.npy. ONLY pass this when the source "
                            "motion_263 is in NORMALIZED scale. qual_eval.py and "
                            "gt_vq_roundtrip.py already denormalize before saving "
                            "generated.npz, so passing --mean/--std on those "
                            "outputs DOUBLE-denormalizes and squashes root motion "
                            "to dataset mean (all bodies look like 'walk forward "
                            "in +z at 0.17 m/s'). Pass only if you have a custom "
                            "pipeline that saves normalized motion_263.")
        p.add_argument("--std", type=Path, default=None,
                       help="HumanML3D std.npy — paired with --mean. Same caveat: "
                            "do not pass on qual_eval / gt_vq_roundtrip outputs.")
        p.add_argument("--use-recovery", action="store_true",
                       help="Force motion_263 → recover_from_ric path even for "
                            "real samples. Useful for validating that the "
                            "encoder round-trips correctly.")
    return parser


def main() -> None:
    args = build_parser().parse_args()

    if args.output_dir is None:
        timestamp = datetime.now().strftime("%Y-%m-%d_%H%M%S")
        output_dir = Path("runs/visualizations") / f"{timestamp}_{args.source}"
    else:
        output_dir = args.output_dir

    mean = np.load(args.mean).astype(np.float32) if args.mean else None
    std = np.load(args.std).astype(np.float32) if args.std else None

    if args.source == "real":
        samples = load_real_samples(args.data_dir, args.seq_ids, args.num_samples)
    else:
        samples = load_generated_samples(args.run_dir)
        if args.seq_ids:
            wanted = set(args.seq_ids)
            selected = [s for s in samples if s["seq_id"] in wanted]
            missing = wanted - {s["seq_id"] for s in selected}
            if missing:
                print(f"  [warn] missing seq_ids in generated.npz: {sorted(missing)}")
            samples = selected
        elif args.num_samples is not None:
            samples = samples[: args.num_samples]

    run_visualization(
        samples, output_dir, args.source, fps=args.fps,
        mean=mean, std=std, use_recovery=args.use_recovery,
    )


if __name__ == "__main__":
    main()
